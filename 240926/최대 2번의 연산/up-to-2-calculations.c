#include <stdio.h>

int main() {
    int a;

    // 정수 a를 입력받습니다.
    scanf("%d", &a);

    // 과정 1: 짝수일 경우 2로 나누기
    if (a % 2 == 0) {
        a = a / 2;  // 정수 나누기
    }
    // 과정 2: 홀수일 경우 1을 더한 뒤 2로 나누기
    else {
        a = (a + 1) / 2;  // 정수 나누기
    }

    // 과정 3: 두 번째 연산을 수행합니다.
    if (a % 2 == 0) {
        a = a / 2;  // 정수 나누기
    }
    else {
        a = (a + 1) / 2;  // 정수 나누기
    }

    // 최종 결과 출력
    printf("%d\n", a);

    return 0;
}